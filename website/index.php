                        <h2>Open Data</h2>

                        <h3>Das Prinzip</h3>

                        <p>Open Data steht für <a href="http://opendefinition.org/okd/" title="Die Open Knowledge Definition erklärt die Ansprüche an Open Data" target="_blank">frei zugängliche, für jegliche Nutzung kostenlose sowie maschinell auswertbare</a> Daten. Diese Daten können aus der Öffentlichen Hand stammen, aber auch von Unternehmen oder anderen Organisationen bereit gestellt werden.</p>

                        <p>Die Öffnung von Datenbeständen im Sinne von Open Data ermöglicht <strong>neue Anwendungen</strong> wie Visualisierungen, Verknüpfung mit anderen Daten, neue Services für die Nutzer, sowie <strong>mehr Transparenz</strong>: Was geschieht mit unseren Steuergeldern? Wie gut ist die Luft, die wir atmen oder das Wasser, das wir trinken?</p>


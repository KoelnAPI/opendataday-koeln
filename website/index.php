                        <p>In Deutschland sammeln einzelne Städte und Bundesländer erste Erfahrungen mit der Offenlegung von Datenbeständen aus der Verwaltung. Am Prototypen einer bundesweiten Open-Data-Plattform wird zur Zeit gearbeitet. Darüber hinaus veröffentlichen einige Behörden und Organisationen Daten aus verschiedenen Bereichen.</p>

                        <p>Wir stehen hier sicherlich noch am <strong>Anfang einer Entwicklung</strong>. Um diese Entwicklung zu fördern, gibt es Veranstaltungen wie den Open Data Day. Hier soll der Nutzen Offener Daten deutlich gemacht werden und auch gezeigt werden, wo uns Offene Daten noch fehlen.</p>

                        <h3>Open Data Überblick</h3>
                        <p>Eine Zusammenstellung von Datenangeboten mit spezieller Berücksichtigung der Relevanz für die Kölner Umgebung bzw. NRW. Leider genügen die Lizenzbedingungen oft nicht den Ansprüchen an Open Data. Es ist daher im Einzelfall auf die Nutzungsbedingungen zu achten.</p>
                    </div>
                </div>

                <div class="row">
                    <div class="span2 offset1"><strong>Stadt Köln</strong></div>
                    <div class="span8">
                        <p>Die Stadt Köln hat sich das Ziel gesetzt, die Veröffentlichung von Datenbeständen zur Regel zu machen. Auf der Website <a href="http://www.offenedaten-koeln.de/">offenedaten-koeln.de</a> gibt es bereits einige Datenangebote: Haushaltsdaten (<a href="http://www.offenedaten-koeln.de/offene-daten/?did=2" target="_blank">Gesamtergebnisplan</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=3" target="_blank">Gesamtfinanzplan</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=4" target="_blank">Teilergebnisplan</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=5" target="_blank">Teilfinanzplan</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=6" target="_blank">Bezirke Einzeldarstellung</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=7" target="_blank">Bezirke Produktgruppen</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=8" target="_blank">Ergebnisplan Produktbereichssumen</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=9" target="_blank">Finanzplan Produktbereichssummen</a>), <a href="http://www.offenedaten-koeln.de/offene-daten/?did=20" target="_blank">Staus (API)</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=10" target="_blank">Veranstaltungen der Stadt Köln (API)</a>, Geodaten (<a href="http://www.offenedaten-koeln.de/offene-daten/?did=14" target="_blank">Stadtbezirke</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=15" target="_blank">Stadtteile</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=16" target="_blank">Stadtteilshapes</a>, <a href="http://www.offenedaten-koeln.de/offene-daten/?did=17" target="_blank">Stadtbezirksgeometrie</a>).</p>
                    </div>
                </div>

                <div class="row">
                    <div class="span2 offset1"><strong>Offenes Köln</strong></div>
                    <div class="span8">
                        <p>Die Plattform <a href="http://offeneskoeln.de/" target="_blank">Offenes Köln</a> bietet Open Data &raquo;von unten&laquo; und stellt Inhalte aus dem zur Kölner Stadtrat und anderen lokalpolitischen Gremien über eine offene <a href="http://offeneskoeln.de/api/" target="_blank">API</a> zur Verfügung. Darüber hinaus finden sich im Blog von Offenes Köln unter dem Stichwort <a href="http://blog.offeneskoeln.de/tag/datendienstag/" target="_blank">Datendienstag</a> weitere interessante Datensätze, die im Sinne von Open Data aufbereitet wurden.</p>
